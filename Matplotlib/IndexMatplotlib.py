'''
Apa itu Matplotlib?
Matplotlib adalah pustaka plotting grafik tingkat rendah dengan 
python yang berfungsi sebagai utilitas visualisasi.

Matplotlib dibuat oleh John D. Hunter.

Matplotlib adalah open source dan kita dapat menggunakannya 
secara bebas.

Matplotlib sebagian besar ditulis dalam python, beberapa segmen 
ditulis dalam C, Objective-C dan Javascript untuk kompatibilitas Platform.
'''

'''
Instalasi Matplotlib
Jika Anda sudah menginstal Python dan PIP pada suatu sistem, maka instalasi Matplotlib sangat mudah.

Instal menggunakan perintah ini:
'''
'pip install matplotlib'

'''
Impor Matplotlib
Setelah Matplotlib diinstal, impor di aplikasi Anda dengan menambahkan pernyataan:import module
'''
